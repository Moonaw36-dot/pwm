use winit::keyboard::{Key as WinitKey, KeyLocation, NamedKey};

pub fn to_imgui_key(key: winit::keyboard::Key, location: KeyLocation) -> Option<imgui::Key> {
    match (key.as_ref(), location) {
        (WinitKey::Named(NamedKey::Tab), _) => Some(imgui::Key::Tab),
        (WinitKey::Named(NamedKey::ArrowLeft), _) => Some(imgui::Key::LeftArrow),
        (WinitKey::Named(NamedKey::ArrowRight), _) => Some(imgui::Key::RightArrow),
        (WinitKey::Named(NamedKey::ArrowUp), _) => Some(imgui::Key::UpArrow),
        (WinitKey::Named(NamedKey::ArrowDown), _) => Some(imgui::Key::DownArrow),
        (WinitKey::Named(NamedKey::PageUp), _) => Some(imgui::Key::PageUp),
        (WinitKey::Named(NamedKey::PageDown), _) => Some(imgui::Key::PageDown),
        (WinitKey::Named(NamedKey::Home), _) => Some(imgui::Key::Home),
        (WinitKey::Named(NamedKey::End), _) => Some(imgui::Key::End),
        (WinitKey::Named(NamedKey::Insert), _) => Some(imgui::Key::Insert),
        (WinitKey::Named(NamedKey::Delete), _) => Some(imgui::Key::Delete),
        (WinitKey::Named(NamedKey::Backspace), _) => Some(imgui::Key::Backspace),
        (WinitKey::Named(NamedKey::Space), _) => Some(imgui::Key::Space),
        (WinitKey::Named(NamedKey::Enter), KeyLocation::Standard) => Some(imgui::Key::Enter),
        (WinitKey::Named(NamedKey::Enter), KeyLocation::Numpad) => Some(imgui::Key::KeypadEnter),
        (WinitKey::Named(NamedKey::Escape), _) => Some(imgui::Key::Escape),
        (WinitKey::Named(NamedKey::Control), KeyLocation::Left) => Some(imgui::Key::LeftCtrl),
        (WinitKey::Named(NamedKey::Control), KeyLocation::Right) => Some(imgui::Key::RightCtrl),
        (WinitKey::Named(NamedKey::Shift), KeyLocation::Left) => Some(imgui::Key::LeftShift),
        (WinitKey::Named(NamedKey::Shift), KeyLocation::Right) => Some(imgui::Key::RightShift),
        (WinitKey::Named(NamedKey::Alt), KeyLocation::Left) => Some(imgui::Key::LeftAlt),
        (WinitKey::Named(NamedKey::Alt), KeyLocation::Right) => Some(imgui::Key::RightAlt),
        (WinitKey::Named(NamedKey::Super), KeyLocation::Left) => Some(imgui::Key::LeftSuper),
        (WinitKey::Named(NamedKey::Super), KeyLocation::Right) => Some(imgui::Key::RightSuper),
        (WinitKey::Named(NamedKey::ContextMenu), _) => Some(imgui::Key::Menu),
        (WinitKey::Named(NamedKey::F1), _) => Some(imgui::Key::F1),
        (WinitKey::Named(NamedKey::F2), _) => Some(imgui::Key::F2),
        (WinitKey::Named(NamedKey::F3), _) => Some(imgui::Key::F3),
        (WinitKey::Named(NamedKey::F4), _) => Some(imgui::Key::F4),
        (WinitKey::Named(NamedKey::F5), _) => Some(imgui::Key::F5),
        (WinitKey::Named(NamedKey::F6), _) => Some(imgui::Key::F6),
        (WinitKey::Named(NamedKey::F7), _) => Some(imgui::Key::F7),
        (WinitKey::Named(NamedKey::F8), _) => Some(imgui::Key::F8),
        (WinitKey::Named(NamedKey::F9), _) => Some(imgui::Key::F9),
        (WinitKey::Named(NamedKey::F10), _) => Some(imgui::Key::F10),
        (WinitKey::Named(NamedKey::F11), _) => Some(imgui::Key::F11),
        (WinitKey::Named(NamedKey::F12), _) => Some(imgui::Key::F12),
        (WinitKey::Named(NamedKey::CapsLock), _) => Some(imgui::Key::CapsLock),
        (WinitKey::Named(NamedKey::ScrollLock), _) => Some(imgui::Key::ScrollLock),
        (WinitKey::Named(NamedKey::NumLock), _) => Some(imgui::Key::NumLock),
        (WinitKey::Named(NamedKey::PrintScreen), _) => Some(imgui::Key::PrintScreen),
        (WinitKey::Named(NamedKey::Pause), _) => Some(imgui::Key::Pause),
        (WinitKey::Character("0"), KeyLocation::Standard) => Some(imgui::Key::Alpha0),
        (WinitKey::Character("1"), KeyLocation::Standard) => Some(imgui::Key::Alpha1),
        (WinitKey::Character("2"), KeyLocation::Standard) => Some(imgui::Key::Alpha2),
        (WinitKey::Character("3"), KeyLocation::Standard) => Some(imgui::Key::Alpha3),
        (WinitKey::Character("4"), KeyLocation::Standard) => Some(imgui::Key::Alpha4),
        (WinitKey::Character("5"), KeyLocation::Standard) => Some(imgui::Key::Alpha5),
        (WinitKey::Character("6"), KeyLocation::Standard) => Some(imgui::Key::Alpha6),
        (WinitKey::Character("7"), KeyLocation::Standard) => Some(imgui::Key::Alpha7),
        (WinitKey::Character("8"), KeyLocation::Standard) => Some(imgui::Key::Alpha8),
        (WinitKey::Character("9"), KeyLocation::Standard) => Some(imgui::Key::Alpha9),
        (WinitKey::Character("0"), KeyLocation::Numpad) => Some(imgui::Key::Keypad0),
        (WinitKey::Character("1"), KeyLocation::Numpad) => Some(imgui::Key::Keypad1),
        (WinitKey::Character("2"), KeyLocation::Numpad) => Some(imgui::Key::Keypad2),
        (WinitKey::Character("3"), KeyLocation::Numpad) => Some(imgui::Key::Keypad3),
        (WinitKey::Character("4"), KeyLocation::Numpad) => Some(imgui::Key::Keypad4),
        (WinitKey::Character("5"), KeyLocation::Numpad) => Some(imgui::Key::Keypad5),
        (WinitKey::Character("6"), KeyLocation::Numpad) => Some(imgui::Key::Keypad6),
        (WinitKey::Character("7"), KeyLocation::Numpad) => Some(imgui::Key::Keypad7),
        (WinitKey::Character("8"), KeyLocation::Numpad) => Some(imgui::Key::Keypad8),
        (WinitKey::Character("9"), KeyLocation::Numpad) => Some(imgui::Key::Keypad9),
        (WinitKey::Character("a"), _) => Some(imgui::Key::A),
        (WinitKey::Character("b"), _) => Some(imgui::Key::B),
        (WinitKey::Character("c"), _) => Some(imgui::Key::C),
        (WinitKey::Character("d"), _) => Some(imgui::Key::D),
        (WinitKey::Character("e"), _) => Some(imgui::Key::E),
        (WinitKey::Character("f"), _) => Some(imgui::Key::F),
        (WinitKey::Character("g"), _) => Some(imgui::Key::G),
        (WinitKey::Character("h"), _) => Some(imgui::Key::H),
        (WinitKey::Character("i"), _) => Some(imgui::Key::I),
        (WinitKey::Character("j"), _) => Some(imgui::Key::J),
        (WinitKey::Character("k"), _) => Some(imgui::Key::K),
        (WinitKey::Character("l"), _) => Some(imgui::Key::L),
        (WinitKey::Character("m"), _) => Some(imgui::Key::M),
        (WinitKey::Character("n"), _) => Some(imgui::Key::N),
        (WinitKey::Character("o"), _) => Some(imgui::Key::O),
        (WinitKey::Character("p"), _) => Some(imgui::Key::P),
        (WinitKey::Character("q"), _) => Some(imgui::Key::Q),
        (WinitKey::Character("r"), _) => Some(imgui::Key::R),
        (WinitKey::Character("s"), _) => Some(imgui::Key::S),
        (WinitKey::Character("t"), _) => Some(imgui::Key::T),
        (WinitKey::Character("u"), _) => Some(imgui::Key::U),
        (WinitKey::Character("v"), _) => Some(imgui::Key::V),
        (WinitKey::Character("w"), _) => Some(imgui::Key::W),
        (WinitKey::Character("x"), _) => Some(imgui::Key::X),
        (WinitKey::Character("y"), _) => Some(imgui::Key::Y),
        (WinitKey::Character("z"), _) => Some(imgui::Key::Z),
        (WinitKey::Character("'"), _) => Some(imgui::Key::Apostrophe),
        (WinitKey::Character(","), KeyLocation::Standard) => Some(imgui::Key::Comma),
        (WinitKey::Character("-"), KeyLocation::Standard) => Some(imgui::Key::Minus),
        (WinitKey::Character("-"), KeyLocation::Numpad) => Some(imgui::Key::KeypadSubtract),
        (WinitKey::Character("."), KeyLocation::Standard) => Some(imgui::Key::Period),
        (WinitKey::Character("."), KeyLocation::Numpad) => Some(imgui::Key::KeypadDecimal),
        (WinitKey::Character("/"), KeyLocation::Standard) => Some(imgui::Key::Slash),
        (WinitKey::Character("/"), KeyLocation::Numpad) => Some(imgui::Key::KeypadDivide),
        (WinitKey::Character(";"), _) => Some(imgui::Key::Semicolon),
        (WinitKey::Character("="), KeyLocation::Standard) => Some(imgui::Key::Equal),
        (WinitKey::Character("="), KeyLocation::Numpad) => Some(imgui::Key::KeypadEqual),
        (WinitKey::Character("["), _) => Some(imgui::Key::LeftBracket),
        (WinitKey::Character("\\"), _) => Some(imgui::Key::Backslash),
        (WinitKey::Character("]"), _) => Some(imgui::Key::RightBracket),
        (WinitKey::Character("`"), _) => Some(imgui::Key::GraveAccent),
        (WinitKey::Character("*"), KeyLocation::Numpad) => Some(imgui::Key::KeypadMultiply),
        (WinitKey::Character("+"), KeyLocation::Numpad) => Some(imgui::Key::KeypadAdd),
        _ => None,
    }
}
